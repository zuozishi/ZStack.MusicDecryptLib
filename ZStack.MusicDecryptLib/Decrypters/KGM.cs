    /// <inheritdoc />
    public void CheckSupport(Stream inputStream)
    {
        CheckSupportEx(inputStream);
    }

    /// <inheritdoc />
    public AudioFormat DetectAudioFormat(Stream inputStream)
    {
        var bytes = new byte[8];
        using var ms = new MemoryStream(bytes);
        DecryptStreamAsync(inputStream, ms, decryptLength: bytes.Length).GetAwaiter().GetResult();
        return AudioUtils.GetAudioFormat(bytes)
            ?? throw new MusicDecryptException("不支持的音频格式");
    }

    /// <inheritdoc />
    public long GetDecryptedSize(Stream inputStream)
    {
        if (!inputStream.CanRead)
            throw new ArgumentException("输入流不可读", nameof(inputStream));
        if (!inputStream.CanSeek)
            throw new ArgumentException("输入流不支持定位", nameof(inputStream));

        var (headerLen, _, _) = CheckSupportEx(inputStream);
        return inputStream.Length - headerLen;
    }

    /// <inheritdoc />
    public async Task DecryptStreamAsync(
        Stream inputStream, Stream outputStream,
        long offset = 0, long decryptLength = -1,
        int bufferSize = 81920, Action<long, long>? progress = null, CancellationToken cancellationToken = default)
    {
        if (!outputStream.CanWrite)
            throw new ArgumentException("输出流不可写", nameof(outputStream));
        if (offset < 0)
            throw new ArgumentException("偏移量不能为负数", nameof(offset));

        var (headerLen, _, isVpr) = CheckSupportEx(inputStream);

        inputStream.Seek(0x1C, SeekOrigin.Begin);
        byte[] key = new byte[17];
        await inputStream.ReadAsync(key, 0, 0x10, cancellationToken);
        key[16] = 0;

        long dataStartPos = headerLen;
        long dataLength = inputStream.Length - headerLen;

        if (decryptLength < 0)
            decryptLength = dataLength - offset;

        if (offset >= dataLength)
            throw new ArgumentException("偏移量超出数据范围", nameof(offset));
        if (offset + decryptLength > dataLength)
            throw new ArgumentException("解密长度超出数据范围", nameof(decryptLength));

        inputStream.Seek(dataStartPos + offset, SeekOrigin.Begin);

        byte[] buffer = new byte[bufferSize];
        long currentOffset = offset;
        long remainingBytes = decryptLength;
        int bytesRead;

        while (remainingBytes > 0 && (bytesRead = await inputStream.ReadAsync(buffer.AsMemory(0, (int)Math.Min(bufferSize, remainingBytes)), cancellationToken)) > 0)
        {
            DecryptBuffer(buffer, key, bytesRead, currentOffset, isVpr);
            await outputStream.WriteAsync(buffer.AsMemory(0, bytesRead), cancellationToken);
            currentOffset += bytesRead;
            remainingBytes -= bytesRead;
            progress?.Invoke(currentOffset - offset, decryptLength);
        }
    }

    private (uint HeaderLen, bool IsKgm, bool IsVpr) CheckSupportEx(Stream inputStream)
    {
        if (!inputStream.CanRead)
            throw new ArgumentException("输入流不可读", nameof(inputStream));
        if (!inputStream.CanSeek)
            throw new ArgumentException("输入流不支持定位", nameof(inputStream));

        inputStream.Seek(0, SeekOrigin.Begin);
        byte[] header = new byte[0x10];
        int headerRead = inputStream.Read(header);
        if (headerRead < 0x10)
            throw new MusicDecryptException("文件头部不完整");

        bool isKgm = header.SequenceEqual(KGM_HEADER);
        bool isVpr = header.SequenceEqual(VPR_HEADER);
        if (!isKgm && !isVpr)
            throw new MusicDecryptException("不是有效的KGM/VPR文件");

        byte[] headerLenBytes = new byte[4];
        inputStream.Read(headerLenBytes);
        uint headerLen = BitConverter.ToUInt32(headerLenBytes, 0);

        if (headerLen < 0x1C + 0x10)
            throw new MusicDecryptException("文件头长度无效");

        inputStream.Seek(20, SeekOrigin.Begin);
        uint mode = inputStream.ReadUInt32();
        if (mode == 5)
            throw new MusicDecryptException("不支持的加密模式");

        return (headerLen, isKgm, isVpr);
    }

    private static void DecryptBuffer(byte[] buffer, byte[] key, int size, long offset, bool isVpr)
    {
        for (int i = 0; i < size; ++i)
        {
            long pos = offset + i;
            byte med8 = (byte)(key[pos % 17] ^ buffer[i]);
            med8 ^= (byte)((med8 & 0xf) << 4);
            byte msk8 = GetMask(pos);
            msk8 ^= (byte)((msk8 & 0xf) << 4);
            buffer[i] = (byte)(med8 ^ msk8);
            if (isVpr)
            {
                buffer[i] ^= VPR_MASK_DIFF[pos % 17];
            }
        }
    }

    private static byte GetMask(long pos)
    {
        long offset = pos >> 4;
        byte value = 0;
        while (offset >= 0x11)
        {
            value ^= TABLE1[offset % 272];
            offset >>= 4;
            value ^= TABLE2[offset % 272];
            offset >>= 4;
        }
        return (byte)(MASK_V2_PREDEF[pos % 272] ^ value);
    }

